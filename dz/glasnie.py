russian = input('Введите любое русское слово: ')
cp = 0
cs = 0

for i in range(len(russian)):
    if russian[i] in ['а','о','э','е','и','ы','у','ё','ю','я']:
        cp += 1
    elif russian[i] in ['б','в','г','д','ж','з','й','к','л','м','н','п','р','с','т','ф','х','ц','ч','ш','щ']:
        cs += 1
        
print('Кол-во глассных: ',cp)
print('Кол-во соглассных: ',cs)
